public class AreaAndPerimeterOfARectangle {
    public static void main(String[] args) {
        double perimeter = 2 * (4.5 + 7.9);
        double area = 45 * 79 / 100.0;
        System.out.println("The perimeter of width of " + 4.5 + " and height of " + 7.9 + " is " + perimeter);
        System.out.println("The area of width of " + 4.5 + " and height of " + 7.9 + " is " + area);
    }
}
