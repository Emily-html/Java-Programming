public class TrigonometricFunctions {
    public static void main(String[] args) {
        System.out.println("The value of sin(PI/2): " + Math.sin(Math.PI / 2));
        System.out.println("The value of cos(PI/2): " + Math.cos(Math.PI / 2));
        System.out.println("The value of tan(0): " + Math.tan(0));
        System.out.println("The value of asin(a): " + Math.asin(0.5));
        System.out.println("The value of acos(a): " + Math.acos(0.5));
        System.out.println("The value of atan(a): " + Math.atan(1));

    }
}
