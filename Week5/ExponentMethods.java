public class ExponentMethods {
    public static void main(String[] args) {
        System.out.println("The value of exp(3.5) is:" + Math.exp(3.5));
        System.out.println("The value of log(3.5) is:" + Math.log(3.5));
        System.out.println("The value of log10(3.5) is:" + Math.log10(3.5));
        System.out.println("The value of pow(3,5) is:" + Math.pow(3, 2));
        System.out.println("The value of sqrt(10.5) is:" + Math.sqrt(10.5));
        System.out.println("The value of sart(4) is:" + Math.sqrt(4));
    }
}
