public class StringConcat {
    public static void main(String[] args) {
        String message = "Welcome" + "to " + "Java";
        String s = "Chapter" + 2;
        String s1 = "Seperate" + "8";
        System.out.println(message);
        System.out.println(s);
        System.out.println(s1);
    }
}
