public class AreaAndPerimeterOfACircle {
    public static void main(String[] args) {
        double perimerter = 2 * 3.14159 * 5.5;
        double area = 3.14159 * 5.5 * 5.5;
        System.out.println("The perimeter of radius of " + 5.5 + " is " + perimerter + " and the area of radius of "
                + 5.5 + " is " + area);
    }
}
