public class AlgebraLinearEquations {
    public static void main(String[] args) {
        double x, y;
        x = (44.5 * 0.55 - 50.2 * 5.9) / (3.4 * 0.55 - 2.1 * 50.2);
        y = (3.4 * 5.9 - 44.5 * 2.1) / (3.4 * 0.55 - 2.1 * 50.2);
        System.out.println("x is " + x + " y is " + y);
    }
}
